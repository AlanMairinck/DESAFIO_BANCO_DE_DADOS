/*----------------------------------------------------------------------------
QUERY 10
----------------------------------------------------------------------------*/
SELECT F.Nome
     , G.Genero
FROM Filmes F
INNER JOIN FilmesGenero FG
ON F.Id = FG.IdFilme
INNER JOIN Generos G
ON G.Id = FG.IdGenero

/*----------------------------------------------------------------------------
QUERY 11
----------------------------------------------------------------------------*/
SELECT F.Nome
     , G.Genero
FROM Filmes F
INNER JOIN FilmesGenero FG
ON F.Id = FG.IdFilme
INNER JOIN Generos G
ON G.Id = FG.IdGenero
WHERE
	G.Genero = 'Mistério'

/*----------------------------------------------------------------------------
QUERY 12
----------------------------------------------------------------------------*/
SELECT F.Nome
     , A.PrimeiroNome
	 , A.UltimoNome
	 , EF.Papel
FROM Atores A
INNER JOIN ElencoFilme EF
ON A.Id = EF.IdAtor
INNER JOIN Filmes F
ON F.Id = EF.IdFilme